    board([B | C], I1, J, R),
    !.

first_unfilled(X, Y) :-
    member([A, B, 0], X),
    Y = [A, B, 0],
    !.

not_is_row(Cells, Y, Num) :-
    member([X, Y, Z], Cells),
    Z = Num,
    !.

not_is_column(Cells, X, Num) :-
    member([X, Y, Z], Cells),
    Z = Num,
    !.

not_is_square(Cells, StartRow, StartColumn, Num) :-
    member(I, [0, 1, 2]),
    member(J, [0, 1, 2]),
    I1 is StartRow + I,
    J1 is StartColumn + J,
    member([J1, I1, Num], Cells),
    !.
    
replace([], New, I, J, []) :- !.
